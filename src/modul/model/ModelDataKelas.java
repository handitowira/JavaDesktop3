public class ModelDataKelas {

    private String idkelas;
    private String namakelas;
    private String jurusan;
    private String walikelas;
    private String ketuakelas;
    private Integer jumlahsiswa;
    private String letak;

    /**
     * @return the idkelas
     */
    public String getIdkelas() {
        return idkelas;
    }

    /**
     * @param idkelas the idkelas to set
     */
    public void setIdkelas(String idkelas) {
        this.idkelas = idkelas;
    }

    /**
     * @return the namakelas
     */
    public String getNamakelas() {
        return namakelas;
    }

    /**
     * @param namakelas the namakelas to set
     */
    public void setNamakelas(String namakelas) {
        this.namakelas = namakelas;
    }

    /**
     * @return the jurusan
     */
    public String getJurusan() {
        return jurusan;
    }

    /**
     * @param jurusan the jurusan to set
     */
    public void setJurusan(String jurusan) {
        this.jurusan = jurusan;
    }

    /**
     * @return the walikelas
     */
    public String getWalikelas() {
        return walikelas;
    }

    /**
     * @param walikelas the walikelas to set
     */
    public void setWalikelas(String walikelas) {
        this.walikelas = walikelas;
    }

    /**
     * @return the ketuakelas
     */
    public String getKetuakelas() {
        return ketuakelas;
    }

    /**
     * @param ketuakelas the ketuakelas to set
     */
    public void setKetuakelas(String ketuakelas) {
        this.ketuakelas = ketuakelas;
    }

    /**
     * @return the jumlahsiswa
     */
    public Integer getJumlahsiswa() {
        return jumlahsiswa;
    }

    /**
     * @param jumlahsiswa the jumlahsiswa to set
     */
    public void setJumlahsiswa(Integer jumlahsiswa) {
        this.jumlahsiswa = jumlahsiswa;
    }

    /**
     * @return the letak
     */
    public String getLetak() {
        return letak;
    }

    /**
     * @param letak the letak to set
     */
    public void setLetak(String letak) {
        this.letak = letak;
    }

}
